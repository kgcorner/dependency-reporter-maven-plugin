package com.kgcorner.models;


/**
 * Description : Link model for D3.js
 * Author: kumar
 * Created on : 03/03/22
 */

public class Link {
    private int source;
    private int target;

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }
}